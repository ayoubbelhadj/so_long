#include "../include_bonus/so_long_bonus.h"

void	*ft_memset_bonus(void *str, int c, size_t n)
{
	size_t			i;
	unsigned char	*s;

	i = 0;
	s = (unsigned char *)str;
	while (i < n)
	{
		s[i] = (unsigned char)c;
		i++;
	}
	return (str);
}

void	*ft_calloc_bonus(size_t num, size_t size)
{
	void	*str;
	size_t	t;

	t = num * size;
	str = (void *)malloc(t);
	if (!str)
		return (NULL);
	ft_memset_bonus(str, 0, t);
	return (str);
}
